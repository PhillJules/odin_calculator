function addition(a, b){
  return a + b;
}
console.log(addition(5, 2));

function subtraction(a, b){
  return a - b;
}
console.log(subtraction(5, 2));

function multiplication(a, b){
  return a * b;
}
console.log(multiplication(5, 2));

function division(a, b){
  return a / b;
}
console.log(division(10, 2));
